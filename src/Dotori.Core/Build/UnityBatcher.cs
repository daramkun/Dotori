namespace Dotori.Core.Build;

/// <summary>
/// Groups source files into unity batches.
/// Module source files (.cppm, .ixx) are always excluded from unity batches.
/// Files matching the exclude patterns in the unity-build block are also excluded.
/// </summary>
public static class UnityBatcher
{
    private static readonly HashSet<string> ModuleExtensions =
        new(StringComparer.OrdinalIgnoreCase) { ".cppm", ".ixx" };

    /// <summary>
    /// Split source files into unity batches.
    /// </summary>
    /// <param name="sources">All source files (absolute paths).</param>
    /// <param name="moduleSources">Module interface files (excluded from unity).</param>
    /// <param name="excludePatterns">Glob patterns from unity-build exclude block.</param>
    /// <param name="batchSize">Maximum files per unity batch.</param>
    /// <param name="unityDir">Directory where unity .cpp files will be written.</param>
    /// <returns>
    /// Generated unity file paths, plus the list of files excluded from unity batching
    /// (to be compiled individually).
    /// </returns>
    public static (IReadOnlyList<string> UnityFiles, IReadOnlyList<string> NonUnityFiles)
        CreateBatches(
            IReadOnlyList<string> sources,
            IReadOnlyList<string> moduleSources,
            IReadOnlyList<string> excludePatterns,
            int batchSize,
            string unityDir)
    {
        Directory.CreateDirectory(unityDir);

        var moduleSet  = new HashSet<string>(moduleSources, StringComparer.OrdinalIgnoreCase);
        var excluded   = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

        // Always exclude module files
        excluded.UnionWith(moduleSet);

        // Exclude files matching unity-build exclude patterns
        foreach (var pattern in excludePatterns)
        {
            var baseDir = Path.GetDirectoryName(sources.FirstOrDefault() ?? string.Empty) ?? string.Empty;
            foreach (var file in GlobExpander.MatchGlob(baseDir, pattern))
                excluded.Add(file);
        }

        // Also exclude module extensions by extension
        foreach (var src in sources)
            if (ModuleExtensions.Contains(Path.GetExtension(src)))
                excluded.Add(src);

        var nonUnityFiles = sources.Where(s => excluded.Contains(s)).ToList();
        var unityTargets  = sources.Where(s => !excluded.Contains(s)).ToList();

        var unityFiles = new List<string>();
        int batchIndex = 0;

        for (int i = 0; i < unityTargets.Count; i += batchSize)
        {
            var batch     = unityTargets.Skip(i).Take(batchSize).ToList();
            var unityFile = Path.Combine(unityDir, $"unity_{batchIndex}.cpp");

            var lines = new List<string>();
            lines.Add("// AUTO-GENERATED by dotori unity batcher — do not edit");
            foreach (var src in batch)
                lines.Add($"#include \"{EscapeIncludePath(src)}\"");

            File.WriteAllLines(unityFile, lines);
            unityFiles.Add(unityFile);
            batchIndex++;
        }

        return (unityFiles, nonUnityFiles);
    }

    private static string EscapeIncludePath(string path) =>
        path.Replace('\\', '/');
}
